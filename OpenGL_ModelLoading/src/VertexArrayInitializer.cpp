unsigned int VertexArrayInitializer::setupEBO(const unsigned int* indices, unsigned int size)
{
	unsigned int EBO;
	glGenBuffers(1, &EBO);
	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, EBO);
	glBufferData(GL_ELEMENT_ARRAY_BUFFER, size, indices, GL_STATIC_DRAW);
	return EBO;
}

unsigned int VertexArrayInitializer::setupVBO(const float* vertices, unsigned int size)
{
	unsigned int VBO;
	glGenBuffers(1, &VBO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, size, vertices, GL_STATIC_DRAW);
	return VBO;
}

int VertexArrayInitializer::setupCubeVAO(unsigned int& VAO, float* vertices, unsigned int ver_size)
{
	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	setupVBO(vertices, ver_size);

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);

	// normal attribute
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);

	// texture attribute
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
	glEnableVertexAttribArray(2);

	return 0;
}

int VertexArrayInitializer::setupRectangleVAO(unsigned int& VAO, float* vertices, unsigned int ver_size)
{
	unsigned int indices[] = {
	0, 1, 3, // first triangle
	1, 2, 3, // second triangle
	};

	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	setupVBO(vertices, ver_size);
	setupEBO(indices, sizeof(indices));

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);

	// color attribute
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);

	// texture attribute
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
	glEnableVertexAttribArray(2);
	return 0;
}

int VertexArrayInitializer::setupTriangleVAO(unsigned int& VAO, float* vertices, unsigned int ver_size)
{
	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	setupVBO(vertices, ver_size);

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);

	// color attribute
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);
	return 0;
}